package model;

public class LowLevelEnemy extends Enemy {
	
	public LowLevelEnemy(String name, int hitPoints, int defence, int strength, int experience){
		super();
		this.setName(name);
		this.setHitPoints(hitPoints);
		this.setDefence(defence);
		this.setStrength(strength);
		this.setExperience(experience);
	}
	

}
